<h2>Selamat Datang di Sistem Surat Menyurat Ushuluddin</h2>
<h3>Tentang Ushuluddin</h3>
<p>Universitas Islam Negeri Sultan Syarif Kasim Riau berdiri pada  tanggal 9 September 1970 sebagai kelanjutan dari Fakultas Ushuluddin Annur
	yang dinegerikan. Pada tahun 1982, ada 2 Jurusan di Fakultas Ushuluddin, yaitu Jurusan Dakwah  dan Jurusan Akidah Filsafat.
	Menyusul kemudian, pada 6 Juni 1991 dibuka JurusanTafsir Hadis  dan Jurusan Perbandingan Agama . Pada tahun 1996, dibuka
	Jurusan Pengembangan Masyarakat Islam dan Bimbingan dan Penyuluhan Islam. Dua jurusan terakhir ini penyelenggaraan pendidikan dan
	pengelolaannya pada Fakultas Ushuluddin sambil menunggu berdirinya Fakultas Dakwah (dan Ilmu Komunikasi). Kemudian pada tahun 2000,
	dalam rangka persiapan IAIN Susqa Pekanbaru menjadi UIN Suska Riau, Fakultas Ushuluddin membuka Jurusan Administrasi Negara.
	Pada 9 Februari 2005, IAIN Susqa Pekanbaru resmi ditingkatkan statusnya menjadi UIN Suska Riau oleh Presiden Susilo Bambang Yudhoyono
	dan setahun kemudian, Jurusan Administrasi Negara ini menjadi bagian dari Fakultas Ekonomi dan Ilmu Sosial. Jadi, dalam 35 tahun
	perjalanan Fakultas Ushuluddin (1970-2005), terdapat tujuh jurusan/prodi.
	Sekarang, setelah lebih dari 45 tahun keberadaan Fakultas Ushuluddin (1970-2016), dinamikanya tidak hanya terletak pada jumlah sumber
	daya dosen, tetapi juga pada input dan lulusannya. Keberadaannya tetap dibutuhkan untuk menjaga keberlanjutan studi ilmu-ilmu Ushuluddin
	yang telah menjadi alasan historis, alasan ideal dan alasan logis keberadaan IAIN Suska Pekanbaru-UIN Suska Riau.
	Kecenderungan arah globalisasi yang menuntut perguruan tinggi memiliki daya saing tinggi, telah mendorong Fakultas Ushuluddin untuk
	meningkatkan daya saing dengan memaksimalkan potensi yang ada sekaligus memerlukan affirmatif action dari segenap pihak agar
	penyelenggaraan pendidikan tinggi dan pengkajian ilmu-ilmu Ushuluddin dapat berlangsung dengan baik dan maju.
</p><br>
<h3><u>Visi dan Misi</u></h3>
<h4>Visi</h4>
<p>“Terwujudnya Fakultas Ushuluddin sebagai fakultas terkemuka dalam integrasi Ilmu Ushuluddin dengan ipteks-sosbud di Asia Tenggara pada tahun 2019.”</p>
<h4>Misi</h4>
<p>Untuk merealisasikan Visi Fakultas Ushuluddin UIN Suska Riau 2014-2018, disusun Misi dalam pernyataan-pernyataan (mission statements) sebagai berikut.
	<br> <br>
	1.	Menyelenggarakan pembelajaran berbasis integrasi ilmu-ilmu Ushuluddin dengan ipteks-sosbud; <br>
	2.	Menyelenggarakan penelitian dalam pengembangan ilmu-ilmu Ushuluddin berbasis integrasi ipteks-sosbud; <br>
	3.	Menyelenggarakan pengabdian kepada masyarakat dalam bidang ilmu-ilmu Ushuluddin yang terintegrasi dengan ipteks-sosbud.
	<br>
	4.	Menyelenggarakan kelembagaan yang efektif untuk mewujudkan visi dan melaksanakan misi;<br>
	5.	Menyelenggarakan sumber daya manusia yang memenuhi kualifikasi untuk melaksanakan tridharma perguruan tinggi berbasis integrasi ilmu-ilmu Ushuluddin dan ipteks-sosbud di kawasan Asia Tenggara.
	<br>
</p>
